//변수

public class Variable {
 
	public static void main(String[] args) {
	//변수를 만들 때 어떤 테이터타입에 연산을써야하는지 알아야 한다
	//number -> integer ... -1, -2, -3,0,1,2,3,
		int a = 1;
		System.out.println(a);
		
		double b = 1.1; //real number -> double ...  -0.2, -1.0
		System.out.println(b);
		
		String c = "hello world";
		System.out.println(c);
		//데이터 타입에 따라나뉜다 int, double, string 
	}

}
